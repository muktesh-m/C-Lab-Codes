#include<stdio.h>
//difference of two integers and two float
void main(int argc, char const *argv[])
{
    int a,b,c;
    float p,q,r;
    a=6;
    b=2;
    p=150.82;
    q=70.23;
    c=a-b;
    r=p-q;
    printf("The difference of two integers=%d\n",c);
    printf("The difference of two floats=%f",r);

}
