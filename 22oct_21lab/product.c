#include<stdio.h>
//product of two integers and two float
void main(int argc, char const *argv[])
{
    int a,b,c;
    float p,q,r;
    a=4;
    b=6;
    p=3.2;
    q=6.7;
    c=a*b;
    r=p*q;
    printf("The product of two integers=%d\n",c);
    printf("The product of two floats=%f",r);
    
}
